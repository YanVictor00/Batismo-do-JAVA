package Condicoes;

public class LacosDeRepicoes {
    public static void main(String[] args) {

        /*
        * WHILE e For
        *
        *  while ( Condição ) { código repetido até a condição se torne falsa }
        * */

        //WHILE
        int numerosDeClones = 0;
        int MaxDeClones = 40;

        while (numerosDeClones < MaxDeClones){
            numerosDeClones++;
            System.out.println("Clone " + numerosDeClones);
        }
        System.out.println("Acabou o chakra do Narutinho");





    }
}
